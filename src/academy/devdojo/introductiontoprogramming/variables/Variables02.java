package academy.devdojo.introductiontoprogramming.variables;

public class Variables02 {
  public static void main(String[] args) {
    byte age = 120;
    short houseNumber = 1000;
    int counter = 2102192102;
    long serialNumber = 892102192102L;
    float regularSalary = 5000.2F;
    double elonMuskFortune = 22000000.0;
    boolean condition = false;
    char genderMan = 'M';

    System.out.println("Age " + age);
    System.out.println("House number " + houseNumber);
    System.out.println("Counter " + counter);
    System.out.println("Serial number " + serialNumber);
    System.out.println("Regular salary" + regularSalary);
    System.out.println("Elon Musk fortune " + elonMuskFortune);
    System.out.println("Condition " + condition);
    System.out.println("Gender Man " + genderMan);
  }
}
